package com.techelevator.model.marvel;

public class Date {

	private String type;
	private String date;
	public Date() {
	}
	public Date(String type, String date) {
		super();
		this.type = type;
		this.date = date;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public String getDate() {
		return date;
	}
	public void setDate(String date) {
		this.date = date;
	}
	
	
	
}
